namespace App\Http\Controllers\SSC;

use App\Http\Controllers\Controller;
use App\Models\EH\Employee\EmployeePersonnelChange;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\DB;

class DashboardController extends Controller {

    /**
     * Create a new controller instance.
     *
     * @return void
     */
    public function __construct() {
    }

    /**
     * Show the application dashboard.
     *
     * @return \Illuminate\Http\Response
     */
    public function index() {
        $personnel_change_count = EmployeePersonnelChange::where('status', config('constants.EMPLOYEE.PERSONNEL_CHANGE_STATUS.PENDING'))->count();
        return view('ssc.dashboard.index', compact('personnel_change_count'));
    }
}
